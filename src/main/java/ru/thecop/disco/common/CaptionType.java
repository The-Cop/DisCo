package ru.thecop.disco.common;

public enum CaptionType {
    /**
     * Caprion on the top border. If no any, works same as LINE.
     */
    TOP_BORDER,
    /**
     * Adds a new line at the top of container.
     */
    LINE,
    /**
     * Adds a new line and a separator at the top of container.
     */
    SEPARATED_LINE
}
